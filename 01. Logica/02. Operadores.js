// Operadores de Asignacion

    x = y  //Asignacion -- (x=y)
        // Asigna un valor a una variable

    x += y //Asignacion de Adicion -- (x =x+y)
        //Asigna el resultado suma el primer valor con el segundo y lo asigna al primer valor

    x -= y //Asigancion de Sustraccion --(x=x-y)
        //Asigna el resultado de restar el segundo valor al primer valor y lo asigna al primer valor

    x *= y //Asignacion de Multiplicacion -- (x=x*y)
        //Asigna el resultado de multiplicar el primer valor por el segundo y lo asigna al primer valor

    x /= y //Asignacion de Division -- (x=x/y)
        //Asigna el resultado de dividir el primer valor entre el segundo y lo asigna al primer valor

    x %= y //Asignacion de Resto -- (x=x%y)
        //Asigna el resultado de dividir el primer valor entre el segundo y lo asigna al primer valor

    x **= y //Asignacion de Exp√≤nenciacion -- (x=x**y)
        //Asigna el resultado de elevar el primer valor al segundo y lo asigna al primer valor

    x <<= y //Asigancion de Desplazamiento a la Izquierda -- (x=x<<y)
        //Asigna el resultado de desplazar el primer valor a la izquierda el numero de veces que indique el segundo y lo asigna al primer valor

    x >>= y //Asigancion de Desplazamiento a la Derecha -- (x=x>>y)
        //Asigna el resultado de desplazar el primer valor a la derecha el numero de veces que indique el segundo y lo asigna al primer valor

    x >>>= y //Asigancion sin Signo de Desplazamiento a la Derecha -- (x=x>>>y)
        //Asigna el resultado de desplazar el primer valor a la derecha el numero de veces que indique el segundo sin signo y lo asigna al primer valor

    x &= y //Asignacion AND
        //Asigna el resultado de aplicar la operacion AND al primer valor y el segundo y lo asigna al primer valor

    x ^= y //Asignacion XOR
        //Asigna el resultado de aplicar la operacion XOR al primer valor y el segundo y lo asigna al primer valor

    x |= y  //Asignacion OR
        //Asigna el resultado de aplicar la operacion OR al primer valor y el segundo y lo asigna al primer valor

