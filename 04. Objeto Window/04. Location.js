//Location HREF
// sirve para obtener la URL completa de la página actual
document.write(window.location.href) // devuelve la URL completa de la página actual
document.write("<br>")

//Location HostName
// sirve para obtener el nombre del host de la URL actual
document.write(window.location.hostname) // devuelve el nombre del host de la URL actual
document.write("<br>")

//Location Pathname
// sirve para obtener la ruta de la URL actual
document.write(window.location.pathname) // devuelve la ruta de la URL actual
document.write("<br>")

//Location Protocol
// sirve para obtener el protocolo de la URL actual (http, https, ftp, etc.)
document.write(window.location.protocol) // devuelve el protocolo de la URL actual
document.write("<br>")

