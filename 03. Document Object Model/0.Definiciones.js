//DOM
//El DOM es una interfaz de programación de aplicaciones (API) para documentos HTML y XML. Define la estructura lógica de los documentos y la forma en que se accede y manipula un documento. En el caso de HTML, el DOM representa la estructura lógica de un documento y la forma en que el documento es leído y visualizado por el navegador.

